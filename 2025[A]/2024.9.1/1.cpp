#include<bits/stdc++.h>
#define int long long
using namespace std;
int T,m,n,i;
long double k,r0,p[100009];
signed main(){
	freopen("1.in","r",stdin);freopen("1.out","w",stdout);
	cin>>T;
	while(T--){
		scanf("%lld%lld%llf",&m,&n,&k);
		scanf("%llf",&r0);
		for(i=1;i<=n;i++)scanf("%llf",&p[i]);
	}
	fclose(stdin);fclose(stdout);
	return 0;
}
